package codechallenge.com.pathtlr;
public class MatrixObject {
	 int first_dimension = 0;
	 int second_dimension = 0;
	 int calculatedResistance = 0;
	 String ptlr;

	public String getPtlr() {
		return ptlr;
	}

	public void setPtlr(String ptlr) {
		this.ptlr = ptlr;
	}

	public int getCalculatedResistance() {
		return calculatedResistance;
	}

	public void setCalculatedResistance(int calculatedResistance) {
		this.calculatedResistance = calculatedResistance;
	}

	public int getSecond_dimension() {
		return second_dimension;
	}

	public void setSecond_dimension(int second_dimension) {
		this.second_dimension = second_dimension;
	}

	public int getFirst_dimension() {
		return first_dimension;
	}

	public void setFirst_dimension(int first_dimension) {
		this.first_dimension = first_dimension;
	}

}